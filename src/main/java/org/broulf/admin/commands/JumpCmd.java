package org.broulf.admin.commands;

import org.broulf.admin.command.CmdError;
import org.broulf.admin.command.CmdException;
import org.broulf.admin.command.CmdSyntaxError;
import org.broulf.admin.command.Command;
import org.broulf.admin.Feature;

public final class JumpCmd extends Command
{
	public JumpCmd()
	{
		super("jump", "Makes you jump.");
	}
	
	@Override
	public void call(String[] args) throws CmdException
	{
		if(args.length != 0)
			throw new CmdSyntaxError();
		
		if(!Feature.MC.player.isOnGround())
			throw new CmdError("Can't jump in mid-air.");
		
		Feature.MC.player.jump();
	}
	
	@Override
	public String getPrimaryAction()
	{
		return "Jump";
	}
	
	@Override
	public void doPrimaryAction()
	{
		Feature.WURST.getCmdProcessor().process("jump");
	}
}
